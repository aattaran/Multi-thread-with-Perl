use strict;
use warnings;

main(@ARGV);

sub main
{
    print foreach (1..9);
    message("\n");
    print foreach ('a'..'Z', "A".."Z");
    message("\n");
    print (join(',' ,('00'..'31')));
}

sub message
{
    my $m = shift or return;
    print("$m\n");
}

sub error
{
    my $e = shift || 'unkown error';
    print("$0: $e\n");
    exit 0;
}

