use strict;
use warnings;

main(@ARGV);

sub main
{
    my $d = 35;
    my $s = "this is a s";
    message($s);
    message($d);
    message($s . ' ' . $d);
}

sub message
{
    my $m = shift or return;
    print("$m\n");
}

sub error
{
    my $e = shift || 'unkown error';
    print("$0: $e\n");
    exit 0;
}

